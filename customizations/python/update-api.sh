#!/bin/sh

VERSION="$1"
LIBPATH="$2"

if [ -z "$LIBPATH" ]
then
  LIBPATH="libs/python/qumranica_api_connector"
fi

if [ ! -d "$LIBPATH" ]
then
  echo "Error: the path $LIBPATH doesn't exist."
  exit 1
fi

echo `sed -i 's!self._base_path = "http://localhost" if host is None else host!host = "https://api.qumranica.org" if host is None else host\n        self._base_path = host" if host is None else host!g' "$LIBPATH/configuration.py"`
echo `sed -i 's!No description provided (generated by Openapi Generator!Python connector to the [qumranica](https://qumranica.org) [api](https://api.qumranica.org/swagger) (generated by Openapi Generator!g' "$LIBPATH/../README.md"`

exit 0